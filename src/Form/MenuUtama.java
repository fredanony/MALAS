    private void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NO.");
        model.addColumn("NAMA");
        model.addColumn("NO HP");
        model.addColumn("EMAIL");
        model.addColumn("BAYAR/TIDAK");
        model.addColumn("JUMLAH");
        
        //menampilkan data database ke table
        try {
            int no=1;
            String sql = "SELECT * FROM data";
            java.sql.Connection conn=(Connection)conek.GetConnection();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
            }
            load_table.setModel(model);
        } catch (Exception e){
        }
    }
    
    
