    private void load_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_load_tableMouseClicked
        // TODO add your handling code here:
        int baris = load_table.rowAtPoint(evt.getPoint());
        String nama = load_table.getValueAt(baris, 1).toString();
        tName.setText(nama);
        String no = load_table.getValueAt(baris, 2).toString();
        tNo.setText(no);
        
        String email = load_table.getValueAt(baris, 3).toString();
        tEmail.setText(email);
        String bt = load_table.getValueAt(baris, 4).toString();
        jComboBox1.setSelectedItem(bt);
        String jumlah = load_table.getValueAt(baris, 5).toString();
        tJumlah.setText(jumlah);
    }//GEN-LAST:event_load_tableMouseClicked

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
            Connection cn = konek.conek.GetConnection();
            st = cn.createStatement();
            String sql="UPDATE data SET nama = '"+tName.getText()+"',no = '"+tNo.getText()+"',email = '"+tEmail.getText()+"',bt = '"+jComboBox1.getSelectedItem()+"',jumlah = '"+tJumlah.getText()+"' WHERE nama = '"+tName.getText()+"'";
            st.executeUpdate(sql);
            java.sql.Connection conn=(Connection)conek.GetConnection();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "data berhasil di edit");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Perubahan Data Gagal"+e.getMessage());
        }
        load_table();
        kosong();
    }//GEN-LAST:event_btnEditActionPerformed

